class Waypoint():

    def __init__(self, lat=None, lon=None, radius=None, alt=None, name=None, desc=None, role=None, direction=None):
        self.lat = lat
        self.lon = lon
        self.radius = radius
        self.alt = alt
        self.name = name
        self.desc = desc
        self.role = role
        self.direction = direction
        